package com.practice.comparable;

public class Pair2 {
	int first;
    int second;
    public Pair2(int first, int second) {
        this.first = first;
        this.second = second;
    }
	@Override
	public String toString() {
		return "Pair2 [first=" + first + ", second=" + second + "]";
	}
    
}
